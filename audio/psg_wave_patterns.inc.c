#define CREATE_PSG_WAVE(n0,n1,n2,n3,n4,n5,n6,n7,n8,n9,n10,n11,n12,n13,n14,n15)  \
{                                                                               \
    (n0)  | (n1  << 8) | (n2  << 16) | (n3  << 24),                             \
    (n4)  | (n5  << 8) | (n6  << 16) | (n7  << 24),                             \
    (n8)  | (n9  << 8) | (n10 << 16) | (n11 << 24),                             \
    (n12) | (n13 << 8) | (n14 << 16) | (n15 << 24)                              \
}


